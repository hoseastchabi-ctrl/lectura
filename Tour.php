        <p>
          Le soleil se couchait sur Paris, teintant les façades des immeubles d’une lueur dorée. Dans le quartier historique, la Tour Eiffel projetait son ombre sur les rues pavées, tandis que les premiers lampadaires s’allumaient un à un.
        </p>
        <p>
          Marie marchait d’un pas pressé, son manteau noir flottant derrière elle. Elle tenait fermement dans sa main une enveloppe jaunie, découverte ce matin même dans le grenier de sa grand-mère. L’enveloppe contenait une carte ancienne de Paris, marquée de symboles mystérieux et d’une adresse dans le Marais.
        </p>
        <p>
          Alors qu’elle s’approchait de l’adresse indiquée, elle remarqua une vieille boutique d’antiquités, et la vitrine poussiéreuse semblait n’avoir pas été nettoyée depuis des décennies. La cloche de la porte tinta doucement lorsqu’elle entra, faisant sursauter le vieux propriétaire qui somnolait derrière son comptoir.
        </p>
        <p>
          “Ah, vous voilà enfin,” dit-il en se redressant, comme s’il l’attendait. “Je savais que quelqu’un finirait par trouver cette carte.”
        </p>
        <p>
          Marie, surprise par cette réception, hésita un instant avant de répondre. “Vous… vous m’attendiez ?”
        </p>
        <p>
          Le vieil homme sourit, ses yeux pétillant d’une lueur malicieuse. “Pas vous spécifiquement, mais quelqu’un. Cette carte a été cachée il y a plus de cinquante ans, attendant que le bon moment arrive.”
        </p>
